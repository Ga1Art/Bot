TARGET_EUROPEAN_RUSSIA_REGIONS = tuple(
    sorted({name for name in EUROPEAN_RUSSIA_REGION_WEIGHTS.keys() if len(name) > 2})
)


def geography_priority_weight(region: str | None) -> int:
    if not region:
        return 0
    region_text = normalize_text(region)
    if region_text in EUROPEAN_RUSSIA_REGION_WEIGHTS:
        return EUROPEAN_RUSSIA_REGION_WEIGHTS[region_text]
    for name, weight in EUROPEAN_RUSSIA_REGION_WEIGHTS.items():
        if name in region_text or region_text in name:
            return weight
    return 0


def is_target_geography(region: str | None) -> bool:
    return geography_priority_weight(region) > 0


def is_priority_region(region: str | None) -> bool:
    if not region:
        return False
    return region in settings.priority_regions or is_target_geography(region)
